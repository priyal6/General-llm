class MenuItem(BaseModel):
    nm: str
    cnt: Optional[str] = None
    price: Optional[str] = None

class SubTotal(BaseModel):
    subtotal_price: Optional[str] = None
    service_price: Optional[str] = None
    tax_price: Optional[str] = None
    etc: Optional[str] = None

class Total(BaseModel):
    total_price: Optional[str] = None

class ReceiptOutput(BaseModel):
    menu: list[MenuItem]
    sub_total: Optional[SubTotal] = None
    total: Optional[Total] = None

    @field_validator("menu")
    @classmethod
    def menu_not_empty(cls, v):
        assert len(v) > 0, "Menu must have at least one item"
        return v

def run_vlm(pil_image):
    prompt = """You are a receipt parser. Look at this receipt image carefully.
Extract every menu item, subtotal, and total.
Return ONLY valid JSON, no explanation, no markdown backticks:
{
  "menu": [
    {"nm": "item name", "cnt": "1 x", "price": "75,000"}
  ],
  "sub_total": {
    "subtotal_price": "...",
    "service_price": "...",
    "tax_price": "...",
    "etc": "..."
  },
  "total": {
    "total_price": "..."
  }
}"""

    messages = [
        {
            "role": "user",
            "content": [
                {"type": "image", "image": pil_image},
                {"type": "text", "text": prompt}
            ]
        }
    ]

    inputs = processor.apply_chat_template(
        messages,
        tokenize=True,
        add_generation_prompt=True,
        return_dict=True,
        return_tensors="pt"
    ).to(model.device)

    with torch.no_grad():
        output_ids = model.generate(
            **inputs,
            max_new_tokens=1024,
            do_sample=False
        )

    trimmed = output_ids[0][inputs["input_ids"].shape[1]:]
    return processor.decode(trimmed, skip_special_tokens=True)

def extract_json(raw_text):
  raw_text = re.sub(r"```json|```", "", raw_text).strip()
  match = re.search(r"\{.*\}", raw_text, re.DOTALL)
  if match:
    return match.group()
  return None

def evaluate(pred: ReceiptOutput, gt: dict):
    gt_parse = gt["gt_parse"]
    gt_items = gt_parse.get("menu", [])

    print("\n" + "="*50)
    print("RESULTS")
    print("="*50)


    print(f"\nMenu items → Predicted: {len(pred.menu)} | GT: {len(gt_items)}")

    print("\nPredicted:")
    for item in pred.menu:
        print(f"  {item.nm:30s} {str(item.cnt):8s} {item.price}")

    print("\nGround Truth:")
    for item in gt_items:
        print(f"  {item['nm']:30s} {str(item.get('cnt')):8s} {item.get('price')}")


    gt_names = [i["nm"].lower().strip() for i in gt_items]
    pred_names = [i.nm.lower().strip() for i in pred.menu]
    matched = sum(1 for p in pred_names if any(p in g or g in p for g in gt_names))
    name_score = matched / max(len(gt_names), 1)

    gt_total = gt_parse.get("total", {}).get("total_price", "N/A")
    pred_total = pred.total.total_price if pred.total else "N/A"

    print(f"\nName match:  {matched}/{len(gt_names)} = {name_score:.0%}")
    print(f"Total price: Predicted={pred_total} | GT={gt_total} | {'✅' if gt_total == pred_total else '❌'}")

    return {"name_score": name_score, "total_match": gt_total == pred_total}

