def normalize_price(price_str):
    if not price_str:
        return ""
    price_str = price_str.strip().lstrip("-")
    price_str = price_str.replace(".", "").replace(",", "")
    return price_str

def evaluate_normalized(pred: ReceiptOutput, gt: dict):
    gt_parse = gt["gt_parse"]
    gt_items = gt_parse.get("menu", [])

    gt_names = [i["nm"].lower().strip().lstrip("-") for i in gt_items]
    pred_names = [i.nm.lower().strip().lstrip("-") for i in pred.menu]
    matched = sum(1 for p in pred_names if any(p in g or g in p for g in gt_names))
    name_score = matched / max(len(gt_names), 1)


    gt_total = gt_parse.get("total", {}).get("total_price", "")
    pred_total = pred.total.total_price if pred.total else ""
    total_match = normalize_price(gt_total) == normalize_price(pred_total)


    count_match = len(pred.menu) == len(gt_items)

    return {
        "name_score": name_score,
        "total_match": total_match,
        "count_match": count_match,
        "pred_items": len(pred.menu),
        "gt_items": len(gt_items)
    }

def run_on_samples(n=5):

    stream = load_dataset(
        "naver-clova-ix/cord-v2",
        split="test",
        streaming=True
    )

    results = []
    failed = []

    for i, sample in enumerate(stream):
        if i >= n:
            break

        print(f"\n{'='*50}")
        print(f"Sample {i+1}/{n}")
        print(f"{'='*50}")

        image = sample["image"]
        ground_truth = json.loads(sample["ground_truth"])


        temp_ground_truth = ground_truth.copy()
        if "gt_parse" in temp_ground_truth and "menu" in temp_ground_truth["gt_parse"]:
            gt_menu_data = temp_ground_truth["gt_parse"]["menu"]
            if isinstance(gt_menu_data, dict):
                temp_ground_truth["gt_parse"]["menu"] = [gt_menu_data]

        gt_parse = temp_ground_truth["gt_parse"]


        gt_items = gt_parse.get("menu", [])
        gt_total = gt_parse.get("total", {}).get("total_price", "N/A")
        print(f"GT: {len(gt_items)} items | total: {gt_total}")


        try:
            raw_output = run_vlm(image)
            json_str = extract_json(raw_output)

            if not json_str:
                print(" No JSON extracted")
                failed.append(i)
                continue

            validated = ReceiptOutput(**json.loads(json_str))
            scores = evaluate_normalized(validated, temp_ground_truth)

            pred_total = validated.total.total_price if validated.total else "N/A"
            print(f"Pred: {scores['pred_items']} items | total: {pred_total}")
            print(f"Name match: {scores['name_score']:.0%} | "
                  f"Count match: {'Match' if scores['count_match'] else 'Unmatch'} | "
                  f"Total match: {'Match' if scores['total_match'] else 'Unmatch'}")

            results.append(scores)

        except (ValidationError, json.JSONDecodeError) as e:
            print(f" Failed: {e}")
            failed.append(i)


    if results:
        avg_name = sum(r["name_score"] for r in results) / len(results)
        total_matches = sum(1 for r in results if r["total_match"])
        count_matches = sum(1 for r in results if r["count_match"])

        print(f"\n{'='*50}")
        print(f"SUMMARY — {len(results)} samples processed, {len(failed)} failed")
        print(f"{'='*50}")
        print(f"Avg name match score : {avg_name:.0%}")
        print(f"Total price match    : {total_matches}/{len(results)}")
        print(f"Item count match     : {count_matches}/{len(results)}")

# Run on 5 samples first
run_on_samples(n=5)